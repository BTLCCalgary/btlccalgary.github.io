    <p class="headingNoPadding">We Need You!</p>

    <p>The primary role of board directors is to provide oversight and accountability to BTLC
through effective governance. Our directors work together to support the Executive
Director in achieving BTLC&apos;s mission through strategic planning, risk management,
financial oversight, fundraising, and relationship building.</p>

    <p>As a small board, we are also a working board. Between meetings, directors contribute
several hours a month individually and in small working groups on projects and tasks
that support BTLC&apos;s mission and activities.</p>

    <p>Board meetings are held every two months for 1.5 to 2 hours, during the work week. We currently meet virtually.</p>

    <p>Board directors commit to a 2-year term.</p>

    <p class="headingNoPadding">What Could You Bring to the Board?</p>

