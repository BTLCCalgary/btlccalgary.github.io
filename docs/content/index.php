        <p>Established in 2004, The Behaviour Therapy and Learning Centre (BTLC) is a registered charitable organization providing Program Unit Funded (PUF) educational programs designed to meet the unique needs of children with autism and other developmental delays.</p>
        <p>BTLC dedicates itself to providing exceptional education for preschool and kindergarten students with autism and other developmental disabilities using the principals of ABA.  We provide the intensity, individualization and a focus on functional skills needed for our students to achieve the greatest degree of independence possible and develop the capacity to participate in inclusive environments.</p>

