<div class="headingNoPadding">How You Can Help</div>
<br/>

<p>With autism on the rise, the demand for services from BTLC is only expected to increase. Our hope is that we can build BTLC to be able to meet the demand, instead of building a longer waiting list.</p>

<p>Unfortunately, the operating expenses are more than we receive from Alberta Education or PUF funding. Those of us involved with BTLC are also hoping to expand the school to a larger space and to older children – which will enable BTLC to offer more education to more children for a longer period of time. We are already seeing excellent results with our current students due to our specialized teaching approach and would love to see this grow.</p>

<div id="picRight"><img src="images/pic/ready_for_school.jpg" alt="Picture of a boy ready for school"/></div>

<p>In order to meet our goals of continuing to offer these services and to have the resources to expand the program, BTLC needs your help. Donations of any amount are graciously accepted, and as a registered charitable organization we will issue tax receipts for all donations over $10.00.</p>

<p>Any <a href="https://atbcares.benevity.org/community/cause/124-855552477RR0001" target="_blank">donations made online through our website</a> will be generously matched 15% by Alberta Treasury Branches through the ATB Cares program (for more information about this program, visit <a href="https://www.atbcares.com/" target="_blank">ATB Cares</a>). Donations can also be dropped off, mailed or couriered to Suite 100, 2107 Sirocco Drive SW Calgary, T3H 5P1 attention Lisa Saelhof.</p>

<p>Thank you so much for your support. While we know that there are many worthy causes out there, when you support BTLC your gift is directly changing children’s lives and the lives of their families.</p>

<p>Charitable Organization Number: 85555 2477 RR0001</p>

